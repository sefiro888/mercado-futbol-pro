/**
 * Retorna la URL de la imagen de un jugador.
 * Intenta usar el ID de ESPN, y si no existe en la base de datos o el mapeo,
 * cae a la propiedad photo del jugador si la tuviera, o retorna null.
 */
export function playerPhotoUrl(player) {
  if (!player) return null
  
  if (player.photo) {
    return player.photo
  }
  
  // 1. Intentar con TheSportsDB (tiene recortes transparentes para la mayoría)
  const sportsdbUrl = SPORTSDB_PHOTOS[player.slug]
  if (sportsdbUrl) {
    return sportsdbUrl
  }
  
  // 2. Fallback de ESPN (tamaño reducido: 130x100 px, suficiente para cards a 2x)
  const espnId = PLAYER_ESPN_IDS[player.id || player.slug]
  if (espnId) {
    return `https://a.espncdn.com/combiner/i?img=/i/headshots/soccer/players/full/${espnId}.png&w=130&h=100`
  }
  
  return null
}
